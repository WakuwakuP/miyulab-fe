/** リトライ待機時間（ミリ秒） */
export const RETRY_DELAY_MS = 1000

/**
 * 最大リトライ回数
 * 超過した場合は接続を諦め、ユーザーに手動リロードを促す。
 * null の場合は無制限リトライ（現行動作）。
 *
 * 注意: 現在の実装ではこの定数は参照されておらず、
 * 実際には無制限リトライとなっています。
 * 将来的にリトライ回数制限を実装する際は、
 * StreamingManagerProvider.tsx の scheduleRetry 関数を
 * 修正してリトライカウントをチェックする必要があります。
 *
 * 将来的にはエクスポネンシャルバックオフの導入も検討します。
 */
export const MAX_RETRY_COUNT: null | number = null

/**
 * ストリーム接続数の警告閾値
 *
 * ブラウザの同一ドメインに対する WebSocket 同時接続数制限（Chrome: 6 程度）や
 * 全体の上限（一般的に 256）を考慮した警告値。
 * この値を超えるとコンソールに警告を出力する。
 *
 * タグタイムライン × 複数バックエンドで接続数が急増するため、
 * UI 側でのタグ数上限（推奨: 5 タグ以内）と併せて運用する。
 */
export const MAX_STREAM_COUNT_WARNING = 20
